// // Определяем структуру данных для подэтапа взыскания
export interface SubStage {
  id: string; // Уникальный ID подэтапа
  name: string; // Название внутреннего процесса (напр., "Получение документов")
  normative: number; // Норматив времени выполнения в минутах
  executorPosition: string; // Должность исполнителя (связь с StaffType.position)
  repetitions: number; // Количество возможных повторов
}

// // Определяем структуру данных для основного этапа взыскания
export interface Stage {
  id: string; // Уникальный ID этапа
  name: string; // Название этапа (напр., "Этап 1: Начало процесса")
  durationDays: { min: number; max: number }; // Сроки выполнения этапа (от/до дней)
  subStages: SubStage[]; // Массив подэтапов, входящих в этот этап
  // // Добавляем поле для зависимостей
  dependsOn?: string[]; // Массив ID этапов, от которых зависит этот этап (опционально)
  recoveryProbability?: number; // // Вероятность успешного взыскания на этом этапе (0-100), опционально
  writeOffProbability?: number; // // Вероятность списания (неуспешного закрытия) на этом этапе (0-100), опционально
}
